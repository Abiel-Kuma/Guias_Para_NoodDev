# Los operadores en Python se utilizan para realizar comparaciones o validar si algo es True o False.

# Operadores aritméticos
suma = 2 + 4  # El operador "+" sirve para sumar.
resta = 6 - 3  # El operador "-" sirve para restar.
division = 9 / 3  # El operador "/" sirve para dividir.
multiplicacion = 3 * 5  # El operador "*" sirve para multiplicar.

# Operadores condicionales
# ">" : mayor que
# "<" : menor que
# "==" : igual que
# "!=" : diferente que

# Ejemplos de condicionales
var = 2 > 1  # Esto debería ser True porque 2 es mayor que 1.
print(var)

var = 1 < 2  # Esto debería ser True porque 1 es menor que 2.
print(var)

var = 1 == 2  # Esto debería ser False porque 1 no es igual a 2.
print(var)

var = 1 != 2  # Esto debería ser True porque 1 es diferente de 2.
print(var)
